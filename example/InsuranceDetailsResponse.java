package org.example;

public class InsuranceDetailsResponse {
    private String firstName;
    private String lastName;
    private String nino;
    private String dateOfBirth;
    private String postcode;
    private String gender;
    private String maritalstatus;
    private String residentialaddress;
    private String emailaddress;
    private String phoneNumber;
    private String occupation;
    private String employmentStatus;
    private String amountofCover;
    private String beneficiaryNameAndRelationship;

    public String getFirstName() { return firstName; }
    public void setFirstName(String firstName) { this.firstName = firstName; }
    public String getLastName() { return lastName; }
    public void setLastName(String lastName) { this.lastName = lastName; }
    public String getNino() { return nino; }
    public void setNino(String nino) { this.nino = nino; }
    public String getDateOfBirth() { return dateOfBirth; }
    public void setDateOfBirth(String dateOfBirth) { this.dateOfBirth = dateOfBirth; }
    public String getPostcode() { return postcode; }
    public void setPostcode(String postcode) { this.postcode = postcode; }
    public String getGender() { return gender; }
    public void setGender(String gender) { this.gender = gender; }
    public String getMaritalstatus() { return maritalstatus; }
    public void setMaritalstatus(String maritalstatus) { this.maritalstatus = maritalstatus; }
    public String getResidentialaddress() { return residentialaddress; }
    public void setResidentialaddress(String residentialaddress) { this.residentialaddress = residentialaddress; }
    public String getEmailaddress() { return emailaddress; }
    public void setEmailaddress(String emailaddress) { this.emailaddress = emailaddress; }
    public String getPhoneNumber() { return phoneNumber; }
    public void setPhoneNumber(String phoneNumber) { this.phoneNumber = phoneNumber; }
    public String getOccupation() { return occupation; }
    public void setOccupation(String occupation) { this.occupation = occupation; }
    public String getEmploymentStatus() { return employmentStatus; }
    public void setEmploymentStatus(String employmentStatus) { this.employmentStatus = employmentStatus; }
    public String getAmountofCover() { return amountofCover; }
    public void setAmountofCover(String amountofCover) { this.amountofCover = amountofCover; }
    public String getBeneficiaryNameAndRelationship() { return beneficiaryNameAndRelationship; }
    public void setBeneficiaryNameAndRelationship(String beneficiaryNameAndRelationship) { this.beneficiaryNameAndRelationship = beneficiaryNameAndRelationship; }
}

